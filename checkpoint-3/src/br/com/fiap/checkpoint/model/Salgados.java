package br.com.fiap.checkpoint.model;

public class Salgados {

	// Atributos
	private String coxinha;
	private String esfiha;
	private String paoDeQueijo;
	private String baguete;
	private String pizza;

	// Getters and Setters
	public String getCoxinha() {
		return coxinha;
	}

	public void setCoxinha(String coxinha) {
		this.coxinha = coxinha;
	}

	public String getEsfiha() {
		return esfiha;
	}

	public void setEsfiha(String esfiha) {
		this.esfiha = esfiha;
	}

	public String getPaoDeQueijo() {
		return paoDeQueijo;
	}

	public void setPaoDeQueijo(String paoDeQueijo) {
		this.paoDeQueijo = paoDeQueijo;
	}

	public String getBaguete() {
		return baguete;
	}

	public void setBaguete(String baguete) {
		this.baguete = baguete;
	}

	public String getPizza() {
		return pizza;
	}

	public void setPizza(String pizza) {
		this.pizza = pizza;
	}

}
