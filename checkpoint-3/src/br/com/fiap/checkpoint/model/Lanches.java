package br.com.fiap.checkpoint.model;

public class Lanches {

	// Atributos
	private String hamburguer;
	private String mistoQuente;
	private String paoComMortadela;
	private String paoComQueijo;
	private String paoComCarne;

	// Getters and Setters
	public String getHamburguer() {
		return hamburguer;
	}

	public void setHamburguer(String hamburguer) {
		this.hamburguer = hamburguer;
	}

	public String getMistoQuente() {
		return mistoQuente;
	}

	public void setMistoQuente(String mistoQuente) {
		this.mistoQuente = mistoQuente;
	}

	public String getPaoComMortadela() {
		return paoComMortadela;
	}

	public void setPaoComMortadela(String paoComMortadela) {
		this.paoComMortadela = paoComMortadela;
	}

	public String getPaoComQueijo() {
		return paoComQueijo;
	}

	public void setPaoComQueijo(String paoComQueijo) {
		this.paoComQueijo = paoComQueijo;
	}

	public String getPaoComCarne() {
		return paoComCarne;
	}

	public void setPaoComCarne(String paoComCarne) {
		this.paoComCarne = paoComCarne;
	}

}
