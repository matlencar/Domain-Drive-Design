package br.com.fiap.checkpoint.model;

public class Doces {

	private String brigadeiro;
	private String bolo;
	private String donuts;
	private String beijinho;
	private String torta;

	// Getters and Setters
	public String getBrigadeiro() {
		return brigadeiro;
	}

	public void setBrigadeiro(String brigadeiro) {
		this.brigadeiro = brigadeiro;
	}

	public String getBolo() {
		return bolo;
	}

	public void setBolo(String bolo) {
		this.bolo = bolo;
	}

	public String getDonuts() {
		return donuts;
	}

	public void setDonuts(String donuts) {
		this.donuts = donuts;
	}

	public String getBeijinho() {
		return beijinho;
	}

	public void setBeijinho(String beijinho) {
		this.beijinho = beijinho;
	}

	public String getTorta() {
		return torta;
	}

	public void setTorta(String torta) {
		this.torta = torta;
	}

}
